CREATE_LOBBY_DESCRIPTION = """
## Создать новое лобби для голосования

### Параметры:
- **friend_ids**: список ID друзей для приглашения
- **list_data**: данные списка для голосования
  - **name**: название списка
  - **items**: пункты списка (минимум 1, максимум 100)
- **mode**: режим голосования
  - `ranking` — ранжирование (участники расставляют пункты по местам)
  - `random` — случайный выбор (колесо фортуны)
- **voting_duration**: длительность голосования в секундах (30-600)

### Value Object — VotingDuration:
Длительность голосования принимается в секундах, но в ответе возвращается в трёх форматах:
- **seconds**: исходное значение (например, 120)
- **minutes**: в минутах (2.0)
- **formatted**: читаемый вид ("2 мин 0 сек")
"""