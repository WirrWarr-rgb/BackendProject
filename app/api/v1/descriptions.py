LIST_GET_DESCRIPTION = """
## Получить списки с пагинацией, сортировкой и фильтрацией

### Кто что видит:
- **Администратор**: видит списки всех пользователей
- **Пользователь**: видит только свои списки

### Параметры пагинации:
- **page**: номер страницы (начиная с 1)
- **per_page**: количество элементов на странице (1-100)

### Сортировка (sort_by):
- `created_at` — по дате создания (по умолчанию)
- `name` — по названию списка

### Направление сортировки (order):
- `desc` — по убыванию (по умолчанию)
- `asc` — по возрастанию

### Фильтрация:
- **search**: поиск по названию списка (регистронезависимый)
- **created_after**: показать списки, созданные после указанной даты (ISO формат: 2026-05-01T00:00:00)
- **created_before**: показать списки, созданные до указанной даты

### Примеры:
- `/lists/?page=1&per_page=10&sort_by=name&order=asc` — первые 10 списков по алфавиту
- `/lists/?search=фильм&created_after=2026-01-01T00:00:00` — поиск по слову "фильм" за 2026 год
"""

